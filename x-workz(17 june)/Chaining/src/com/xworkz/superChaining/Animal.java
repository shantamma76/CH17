package com.xworkz.superChaining;

public class Animal {
	  private String name;
	  private String color;

	    // Superclass constructor
	    public Animal(String name,String color) 
	    {
	        this.name = name;
	        this.color = color;
	        System.out.println("Animal name:" +name);
	        System.out.println("Animal color:" +color);
	    }
	}


