class WashingMachine
{
	public static void makeNoise()
{
	System.out.println("washing machine making noise");
}
public static void showTime()
{
	System.out.println("washing machine shows the time");
}
public static void rinse()
{
	System.out.println("cloths are rise in washing machine");
}
public static void soak()
{
	System.out.println("cloths are soaking in water");
}
public static void dry()
{
	System.out.println("cloths are drying under sunlight");
}
public static void start()
{
	System.out.println("washing machine start cleaning");
}
public static void stop()
{
	System.out.println("washing machine stop cleaning");
}
public static void work()
{
	System.out.println("washing machine working");
}
}